"""Nutrients (rog-strix) loader — private Russian corpus, anti-parametric.

Corpus: ~107 docs (Russian lecture PDFs + Telegram chat) → chunked. QA: generated by
grok-4.5, grounded in a specific passage (gold_text). Because the lecture PDFs are huge
(≈600 chunks each), the gold is a PASSAGE — retrieval is scored by text overlap with
gold_text (finding the 1 gold chunk among hundreds is the hard part); pollution injects
other docs on top. Matches the LiveRAG loader interface (doc_id / gold_docs / all_docs)
plus gold_evidences for the text-overlap metric.
"""
from __future__ import annotations

import json
import os
import re
from dataclasses import dataclass, field
from functools import lru_cache

_DIR = os.path.join(os.path.dirname(__file__), "..", ".nutridata")


@dataclass(frozen=True)
class Passage:
    id: str
    doc_id: str
    page: int
    text: str

    @property
    def key(self) -> str:
        return self.id


@dataclass(frozen=True)
class Question:
    id: str
    question: str
    answer: str
    gold_docs: frozenset        # {gold_doc} — for pollution scoping + doc-level fallback
    gold_evidences: tuple       # (gold_text,) — the source passage (drives text-overlap metric)
    qtype: str                  # pdf | chat
    difficulty: float
    is_numeric: bool


@dataclass
class Corpus:
    passages: list
    by_doc: dict = field(default_factory=dict)
    all_docs: list = field(default_factory=list)
    docs: dict = field(default_factory=dict)
    companies: dict = field(default_factory=dict)

    def pages_for(self, doc_id: str) -> list:
        return self.by_doc.get(doc_id, [])


def _chunk(text, size=1200, overlap=150):
    if len(text) <= size:
        return [text] if text.strip() else []
    out, i = [], 0
    while i < len(text):
        out.append(text[i:i + size])
        i += size - overlap
    return out


def _num(s):
    return bool(re.search(r"\d", str(s)))


def _root():
    return os.environ.get("NUTRI_DIR", os.path.abspath(_DIR))


def load_questions(root=None) -> list:
    out = []
    with open(os.path.join(_root(), "qa_ru.jsonl"), encoding="utf-8") as f:
        for line in f:
            r = json.loads(line)
            out.append(Question(
                id=r["id"], question=r["question"], answer=str(r["answer"]),
                gold_docs=frozenset({r["gold_doc"]}), gold_evidences=(r["gold_text"],),
                qtype=r.get("source", "pdf"), difficulty=0.0, is_numeric=_num(r["answer"]),
            ))
    return out


def load_corpus(root=None, *, limit_docs=None, chunk_size=1200) -> Corpus:
    passages, by_doc = [], {}
    with open(os.path.join(_root(), "corpus_ru.jsonl"), encoding="utf-8") as f:
        for line in f:
            d = json.loads(line)
            did = d["doc_id"]
            if limit_docs is not None and did not in limit_docs:
                continue
            for ci, c in enumerate(_chunk(d["text"], size=chunk_size)):
                p = Passage(id=f"{did}#c{ci}", doc_id=did, page=ci, text=c)
                passages.append(p)
                by_doc.setdefault(did, []).append(p)
    return Corpus(passages=passages, by_doc=by_doc, all_docs=list(by_doc.keys()),
                  docs={d: {} for d in by_doc})


@lru_cache(maxsize=1)
def default_root() -> str:
    return _root()
